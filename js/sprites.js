export function drawSprite(canvas, spriteData, scale=3){
  const rows = spriteData.length;
  const cols = Math.max(...spriteData.map(r=>r.length));
  canvas.width = cols*scale;
  canvas.height = rows*scale;
  canvas.style.width = (cols*scale)+'px';
  canvas.style.height = (rows*scale)+'px';
  canvas.classList.add('sprite-canvas');
  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;
  for(let y=0;y<rows;y++){
    for(let x=0;x<spriteData[y].length;x++){
      const c = spriteData[y][x];
      if(c!=='.' && PALETTE[c]){
        ctx.fillStyle = PALETTE[c];
        ctx.fillRect(x*scale, y*scale, scale, scale);
      }
    }
  }
}

export function createSpriteEl(name, scale=3){
  const c = document.createElement('canvas');
  if(SPRITES[name]) drawSprite(c, SPRITES[name], scale);
  return c;
}

// Sprite renderizzato su canvas offscreen, per disegnarlo sulla mappa
export function getSpriteImage(name, size){
  const data = SPRITES[name];
  if(!data) return null;
  const rows = data.length;
  const cols = Math.max(...data.map(r=>r.length));
  const c = document.createElement('canvas');
  const scale = Math.max(1, Math.floor(size/Math.max(rows,cols)));
  c.width = cols*scale; c.height = rows*scale;
  const cx = c.getContext('2d');
  cx.imageSmoothingEnabled = false;
  for(let y=0;y<rows;y++){
    for(let x=0;x<data[y].length;x++){
      const ch = data[y][x];
      if(ch!=='.' && PALETTE[ch]){
        cx.fillStyle = PALETTE[ch];
        cx.fillRect(x*scale, y*scale, scale, scale);
      }
    }
  }
  return c;
}
